num1 = float(input("Enter a num : "))
# دریافت ورودی‌ها از کاربر و تبدیل به عدد اعشاری (float)
num2 = float(input(" Enter a num : "))

sum_result = num1 + num2
sub_result = num1 - num2
mul_result = num1 * num2
div_result = num1 / num2  # دقت کن num2 نباید صفر باشه

print(f"{num1} + {num2} = {sum_result}")
print(f"{num1} - {num2} = {sub_result}")
print(f"{num1} * {num2} = {mul_result}")
print(f"{num1} / {num2} = {div_result}")
